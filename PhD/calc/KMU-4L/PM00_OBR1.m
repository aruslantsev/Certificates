function res = PM00_OBR1(E1, E2, G12, nu12, thr, dg)
% 0

P = [  0   50  100  200  300  400  500];
d0=[-113   93  299  690 1050 1386 1677]; 
d1=[ 174  341  523  884 1236 1582 1914]; 
d2=[ -34 -101 -165 -281 -385 -478 -558];
d3=[ -41  112  279  615  940 1253 1525];
d4=[-183 -237 -296 -417 -532 -645 -741]; 
P1 =[0      50  100  200  300   400  500];
d01=[-295 -319 -348 -414 -483  -547 -608];
d11=[-49   130  322  697 1052  1394 1706];
d21=[-17   151  331  685 1019  1351 1676];
d31=[-479 -540 -597 -698 -782  -859 -930];
d41=[69    252  437  793  1125 1441 1737];
P2= [0      50  100  150  200  250  300  350  400  450  500  550];
d02=[2040 2252 2428 2601 2774 2949 3115 3292 3461 3633 3796 3962];
d12=[2050 2271 2451 2625 2799 2972 3142 3315 3490 3663 3826 3988];
d22=[2176 2113 2058 2001 1943 1888 1828 1772 1717 1661 1609 1554];
d32=[2240 2377 2553 2727 2905 3079 3248 3420 3589 3756 3918 4094];
d42=[1764 1695 1631 1568 1506 1446 1387 1327 1275 1214 1160 1100];

b=20;
h=0.5;


sig = P * 10 / (b * h);
e0 = (d0 - d0(1)) * 4 * 10^-6 / 2.16;
e1 = (d1 - d1(1)) * 4 * 10^-6 / 2.16;
e2 = (d2 - d2(1)) * 4 * 10^-6 / 2.16;
e3 = (d3 - d3(1)) * 4 * 10^-6 / 2.16;
e4 = (d4 - d4(1)) * 4 * 10^-6 / 2.16;
eR = (e0 + e1 + e3) / 3;
eS = (e2 + e4) / 2;

sig1 = P1 * 10 / (b * h);
e01 = (d01 - d01(1)) * 4 * 10^-6 / 2.16;
e11 = (d11 - d11(1)) * 4 * 10^-6 / 2.16;
e21 = (d21 - d21(1)) * 4 * 10^-6 / 2.16;
e31 = (d31 - d31(1)) * 4 * 10^-6 / 2.16;
e41 = (d41 - d41(1)) * 4 * 10^-6 / 2.16;
eR1 = (e11 + e21 + e41) / 3;
eS1 = (e01 + e31) / 2;

sig2 = P2 * 10 / (b * h);
e02 = (d02 - d02(1)) * 4 * 10^-6 / 2.16;
e12 = (d12 - d12(1)) * 4 * 10^-6 / 2.16;
e22 = (d22 - d22(1)) * 4 * 10^-6 / 2.16;
e32 = (d32 - d32(1)) * 4 * 10^-6 / 2.16;
e42 = (d42 - d42(1)) * 4 * 10^-6 / 2.16;
eR2 = (e02 + e12 + e32) / 3;
eS2 = (e22 + e42) / 2;

S = [sig sig1 sig2];
ERR = [eR eR1 eR2];
ESS = [eS eS1 eS2];

Alpha=[0];
H_m = [1];
Sigma = 0:50:550;
Ex = [eR eR1 eR2];
Ey = [eS eS1 eS2];
[ExR0, EyR0] = nonlinear_deform(Alpha, H_m, E1, E2, G12, nu12, thr, dg, Sigma);
[ExR1, EyR1] = nonlinear_deform(Alpha, H_m, E1, E2, G12, nu12, thr, 1, Sigma);
[ExR2, EyR2] = nonlinear_deform(Alpha, H_m, E1, E2, G12, nu12, thr, 2, Sigma);
[ExR3, EyR3] = nonlinear_deform(Alpha, H_m, E1, E2, G12, nu12, thr, 3, Sigma);

%plot(ExR0*100,Sigma,'k-x', EyR0*100,Sigma,'k-x', ExR1*100,Sigma,'k-s', EyR1*100,Sigma,'k-s', ExR2*100,Sigma,'k-+', EyR2*100,Sigma,'k-+', ExR3*100,Sigma,'k-v', EyR3*100,Sigma,'k-v', Ex*100,S,'ko', Ey*100,S,'ko')
plot(ExR0*100,Sigma,'b-', EyR0*100,Sigma,'b-', Ex*100,S,'ko', Ey*100,S,'ko')
ylabel('\sigma_x, Ãœ‡')
xlabel('\epsilon_y, \epsilon_x, %')
grid on

res = 0;
